// Get current language from localStorage or default to English
function getCurrentLanguage() {
  return localStorage.getItem('qr_language') || 'en';
}

// Get translation for a key
function t(key) {
  const lang = getCurrentLanguage();
  const trans = translations[lang] || translations.en;
  return trans[key] || translations.en[key] || key;
}

// Set language
function setLanguage(lang) {
  localStorage.setItem('qr_language', lang);
  applyTranslations(lang);
  
  // Update dynamic content
  if (typeof updateThemeDisplay === 'function') {
    const prefs = JSON.parse(localStorage.getItem('qr_prefs') || '{}');
    if (prefs.theme) updateThemeDisplay(prefs.theme);
  }
  
  if (typeof updateSyncUI === 'function' && window.QR && QR.sync) {
    const isConnected = QR.sync.isConnected && QR.sync.isConnected();
    updateSyncUI(isConnected);
  }
  
  // Dispatch event for other pages to listen
  window.dispatchEvent(new CustomEvent('qr:language-changed', { detail: { language: lang } }));
}

// Apply translations to page
function applyTranslations(lang) {
  const t = translations[lang] || translations.en;
  
  // Update all elements with data-i18n attribute
  document.querySelectorAll('[data-i18n]').forEach(el => {
    const key = el.getAttribute('data-i18n');
    if (t[key]) {
      el.innerHTML = t[key];
    }
  });
  
  // Update all elements with data-i18n-placeholder attribute
  document.querySelectorAll('[data-i18n-placeholder]').forEach(el => {
    const key = el.getAttribute('data-i18n-placeholder');
    if (t[key]) {
      el.placeholder = t[key];
    }
  });
  
  // Update HTML lang attribute
  document.documentElement.lang = lang;
  
  // Apply RTL for Arabic
  if (lang === 'ar') {
    document.documentElement.dir = 'rtl';
    document.body.classList.add('rtl');
  } else {
    document.documentElement.dir = 'ltr';
    document.body.classList.remove('rtl');
  }
}

// Initialize language on page load
function initLanguage() {
  const lang = getCurrentLanguage();
  applyTranslations(lang);
  
  // Set language selector if it exists
  const langSelect = document.getElementById('language-select');
  if (langSelect) {
    langSelect.value = lang;
  }
}

// Export functions
if (typeof module !== 'undefined' && module.exports) {
  module.exports = { translations, getCurrentLanguage, setLanguage, applyTranslations, initLanguage };
}
